enum LessonType {
  quran, // سورة قرآنية
  memorization, // حفظ (هوية المسلم، أركان الإيمان)
  hadith, // حديث نبوي شريف
  creed, // عقيدة (أركان الإسلام/الإيمان)
  seerah, // سيرة نبوية
  manners, // آداب إسلامية
}

class IslamicLesson {
  final int id;
  final String title;
  final String subtitle; // وصف مختصر
  final LessonType type;
  final int pageNumber;
  final String icon; // أيقونة
  final List<LessonSection> sections;

  const IslamicLesson({
    required this.id,
    required this.title,
    this.subtitle = '',
    required this.type,
    required this.pageNumber,
    required this.icon,
    required this.sections,
  });
}

class LessonSection {
  final String title; // عنوان القسم
  final String type; // quran, explanation, memorize, discussion, activity, story
  final String content;
  final List<String>? points; // نقاط أو أسئلة

  const LessonSection({
    required this.title,
    required this.type,
    required this.content,
    this.points,
  });
}

